class Solution{   
public:
    int xor_num = 0;
    int getOddOccurrence(int arr[], int n) {
        // code here
        for(int i=0;i<n;++i){
            xor_num ^= arr[i];
        }
        return xor_num;
    }
};

// { Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        int arr[n];
        for (int i = 0; i < n; i++) {
            cin >> arr[i];
        }
        Solution ob;
        auto ans = ob.getOddOccurrence(arr, n);
        cout << ans << "\n";
    }
    return 0;
}

